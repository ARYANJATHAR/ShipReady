/**
 * Error page generators — framework-aware.
 *
 * Generates:
 *   - 404 / not-found page
 *   - Error boundary / 500 page
 *   - Global error (Next.js only)
 *
 * Framework dispatch:
 *   - Next.js / Remix: app/not-found.tsx, app/error.tsx, app/global-error.tsx
 *   - Astro: src/pages/404.astro, src/pages/500.astro
 *   - SvelteKit: src/routes/+error.svelte (single page handles both 404 + 500)
 *   - Vite / unknown: static public/404.html, public/500.html
 */

import type { Framework } from "../framework";

export interface ErrorPageInput {
  projectName: string;
  siteUrl?: string;
  framework: Framework;
}

/** Check if framework uses Next.js-style App Router conventions. */
function hasAppRouter(framework: Framework): boolean {
  return framework === "nextjs" || framework === "remix";
}

/** Check if framework uses SvelteKit conventions. */
function isSvelteKit(framework: Framework): boolean {
  return framework === "sveltekit";
}

/** Check if framework is Astro. */
function isAstro(framework: Framework): boolean {
  return framework === "astro";
}

// ─────────────────────────────────────────────────────────────
//  404 / Not Found
// ─────────────────────────────────────────────────────────────

export function generateNotFound(input: ErrorPageInput): { path: string; content: string } {
  if (hasAppRouter(input.framework)) {
    return generateNotFoundNextjs(input);
  }
  if (isAstro(input.framework)) {
    return generateNotFoundAstro(input);
  }
  if (isSvelteKit(input.framework)) {
    return generateNotFoundSvelteKit(input);
  }
  // Vite / unknown — static HTML
  return generateNotFoundStatic(input);
}

