// ─────────────────────────────────────────────────────────────
//  Error boundary / 500 page
// ─────────────────────────────────────────────────────────────

export function generateErrorPage(input: ErrorPageInput): { path: string; content: string } | null {
  if (hasAppRouter(input.framework)) {
    return generateErrorPageNextjs(input);
  }
  if (isAstro(input.framework)) {
    return generateErrorPageAstro(input);
  }
  // SvelteKit uses the same +error.svelte for both 404 and 500.
  // If it was already generated by generateNotFound, skip.
  // generateNotFound already handles this, so we skip here.
  // The dedup happens in index.ts.
  if (isSvelteKit(input.framework)) {
    return null;
  }
  // Vite / unknown — static HTML
  return generateErrorPageStatic(input);
}

