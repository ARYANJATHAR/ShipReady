// ─────────────────────────────────────────────────────────────
//  Global error (Next.js only — no equivalent elsewhere)
// ─────────────────────────────────────────────────────────────

export function generateGlobalError(input: ErrorPageInput): { path: string; content: string } | null {
  if (input.framework !== "nextjs") {
    // Global error is a Next.js-specific concept. Remix handles this
    // differently, and no other framework has an equivalent.
    return null;
  }

  return {
    path: "app/global-error.tsx",
    content: `"use client";

/**
 * Global error boundary for ${input.projectName}.
 * Auto-generated by ShipReady. This is the LAST line of defense — it
 * catches errors that happen in the root layout itself, where error.tsx
 * can't reach. Must include its own <html> and <body> tags.
 *
 * Docs: https://nextjs.org/docs/app/api-reference/file-conventions/error#global-error
 */
export default function GlobalError({
  error,
  reset,
}: {
  error: Error & { digest?: string };
  reset: () => void;
}) {
  return (
    <html lang="en">
      <body
        style={{
          minHeight: "100vh",
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
          padding: "1.5rem",
          background: "#0a0a09",
          color: "#fafaf9",
          fontFamily: "system-ui, -apple-system, sans-serif",
        }}
      >
        <div style={{ maxWidth: "28rem", textAlign: "center" }}>
          <p
            style={{
              fontFamily: "monospace",
              fontSize: "0.875rem",
              color: "#a8a29e",
              marginBottom: "1rem",
            }}
          >
            Critical error
          </p>
          <h1
            style={{
              fontSize: "2.25rem",
              fontWeight: 600,
              marginBottom: "1rem",
            }}
          >
            Something broke
          </h1>
          <p
            style={{
              color: "#a8a29e",
              marginBottom: "2rem",
              lineHeight: 1.6,
            }}
          >
            The site hit an unrecoverable error. Try refreshing the page.
            If it keeps happening, contact support.
          </p>
          {error.digest && (
            <p
              style={{
                fontFamily: "monospace",
                fontSize: "0.75rem",
                color: "#78716c",
                marginBottom: "1rem",
              }}
            >
              ref: {error.digest}
            </p>
          )}
          <button
            onClick={reset}
            style={{
              padding: "0.5rem 1rem",
              background: "#c4f542",
              color: "#0a0a09",
              border: "none",
              borderRadius: "0.5rem",
              fontWeight: 500,
              cursor: "pointer",
            }}
          >
            Try again
          </button>
        </div>
      </body>
    </html>
  );
}
`,
  };
}
